private void comandoAcaoEuro() {

        double euro;
        String EuroValor;

        PegaDados.replaceAll(",", ".");

        PegaDados = jTextValorConverter.getText();

        double aDouble = Double.parseDouble(PegaDados);

        euro = aDouble * ValorCedulasEuro;

        EuroValor = String.valueOf(euro);

        jLabelResultadoConversao.setText(EuroValor);
    }

